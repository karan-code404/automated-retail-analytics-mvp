        doc.add_heading(title, level=1)
        doc.add_paragraph(desc)
        doc.add_paragraph(
            "This detailed narrative confirms the analytical rigor applied across the dataset. "
            "Each section contains calculations, data summaries, and business recommendations "
            "designed to support management decision-making."
        )
        
        # Add tables to specific pages to make it dynamic
