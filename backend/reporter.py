def add_header_footer(canvas, doc):
    """
    ReportLab canvas callback to render headers, footers, and page numbers.
    Excludes the first page (Cover page).
    """
    canvas.saveState()
    if doc.page > 1:
        # Header text
        canvas.setFont('Helvetica-Bold', 7.5)
        canvas.setFillColor(colors.HexColor('#475569'))
        canvas.drawString(45, 802, "ENTERPRISE BUSINESS INTELLIGENCE AUDIT")
        canvas.drawRightString(550, 802, "CONFIDENTIAL - RESTRICTED REPORT")
        
        # Header line
        canvas.setStrokeColor(colors.HexColor('#cbd5e1'))
        canvas.setLineWidth(0.5)
        canvas.line(45, 794, 550, 794)
        
        # Footer text
        canvas.setFont('Helvetica', 7.5)
        canvas.setFillColor(colors.HexColor('#64748b'))
        canvas.drawString(45, 34, "Generated by Automated Enterprise Analytics Platform")
        canvas.drawRightString(550, 34, f"Page {doc.page} of 53")
        
        # Footer line
        canvas.line(45, 44, 550, 44)
    canvas.restoreState()

